    private void addMenuItemsFromJson()  {
        try {
            String jsonDataString = readJsonDataFromFile();
            JSONArray menuItemsJsonArray = new JSONArray(jsonDataString);

            for (int i = 0; i < menuItemsJsonArray.length(); i++) {

                JSONObject menuItemObject = menuItemsJsonArray.getJSONObject(i);

                String menuItemName = menuItemObject.getString("name");
                String menuItemPrice = menuItemObject.getString("id");
                String menuItemCategory = menuItemObject.getString("email");


                Student student = new Student(menuItemName, menuItemPrice,
                        menuItemCategory);
                mRecyclerViewItems.add(student);
            }
        } catch (JSONException exception) {
            Log.e(MainActivity.class.getName(), "Unable to parse JSON file.", exception);
        }
    }


        public String readJsonDataFromFile() {
            String json;
            try {
                InputStream is = getAssets().open("student.json");
                int size = is.available();
                byte[] buffer = new byte[size];
                is.read(buffer);
                is.close();
                json = new String(buffer, StandardCharsets.UTF_8);
            } catch (IOException ex) {
                ex.printStackTrace();
                return null;
            }
            return json;
        }


